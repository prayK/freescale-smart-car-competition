/***电机pid输出速度***/ 
void MotorControl(void) 
{ 
          Speed_Ek0 = GoalSpeed - PulseCount; 
 
          SpeedOutPut= (int16) (   SpeedOutPut
                                   + (int16)(Speed_Kp * (Speed_Ek0 - Speed_Ek1)) 
                                   + (int16)(Speed_Ki * (Speed_Ek0)) 
                                   + (int16)(Speed_Kd * (Speed_Ek0 - 2 * Speed_Ek1 + Speed_Ek2)) 
                                ); 
          Speed_Ek2 = Speed_Ek1; 
          Speed_Ek1 = Speed_Ek0; 

          if ( SpeedOutPut > 60 ) SpeedOutPut  = 60; 
          else if ( SpeedOutPut < (int16) -60 ) SpeedOutPut = -60; 
 
          if(SpeedOutPut > 0) 
          { 
                  DutyGo = (uint16)SpeedOutPut;
                  DutyBack = 0; 
          } 
          else 
          { 
                  DutyGo = 0; 
                  DutyBack = (uint16)(-SpeedOutPut); 
          } 
          ftm_pwm_duty(FTM0, FTM_CH2, (uint16)DutyGo);
          ftm_pwm_duty(FTM0, FTM_CH3, (uint16)DutyBack); 
          
      #if (0)
          speed_line[0] = (uint32)PulseCount; 
          speed_line[1] = (uint32)GoalSpeed;
          vcan_sendware(speed_line, sizeof(speed_line));
      #endif
} 
 
